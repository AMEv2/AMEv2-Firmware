static void ame_ui_Menu(void)
{
    // Menu Entered
    ESP_LOGI(TAG, "Button pressed");
    // Draw First menu item
    menuIdx = 0;
    ame_ui_DrawMenuItem(menuIdx);

    for (;;)
    {
        if (xQueueReceive(event_queue, &e, pdMS_TO_TICKS(20000)) == pdPASS)
        {
            if (e.type == RE_ET_BTN_CLICKED)
            {
                // Menu Entered
                ESP_LOGI(TAG, "Menu Item %d selected", menuIdx);
                if (menuIdx == NUM_MENU_ITEMS - 1)
                {
                    // Back
                    ESP_LOGI(TAG, "Back - Exit Menu");
                    break;
                }
                else
                {
                    ame_ui_MenuItemSelected(menuIdx);
                }
            }
            else if (e.type == RE_ET_CHANGED)
            {
                // next menu item
                if (e.diff > 0)
                {
                    // next menu item
                    menuIdx++;
                    if (menuIdx >= NUM_MENU_ITEMS)
                    {
                        menuIdx = NUM_MENU_ITEMS - 1;
                    }
                    ame_ui_DrawMenuItem(menuIdx);
                }
                else if (e.diff < 0)
                {
                    // prev menu item
                    menuIdx--;
                    if (menuIdx < 0)
                    {
                        menuIdx = 0;
                    }
                    ame_ui_DrawMenuItem(menuIdx);
                }
            }
            else if (e.type == RE_ET_BTN_LONG_PRESSED)
            {
                // Long pressed, exit menu
                break;
            }
        }
        else
        {
            // timed out
            ESP_LOGI(TAG, "Menu Timed Out");
            break;
        }
    }
}

