/**********************************************************************************************************************
 * Global Function Definitions
 **********************************************************************************************************************/
void AME_UI_Task(void *pvArgs)
{
    gpio_reset_pin(CONFIG_IO_PIN_START_STOP_SWITCH);
    // zero-initialize the config structure.
    gpio_config_t io_conf = {};
    // disable interrupt
    io_conf.intr_type = GPIO_INTR_NEGEDGE;
    // set as output mode
    io_conf.mode = GPIO_MODE_INPUT;
    // bit mask of the pins that you want to set,e.g.GPIO18/19
    io_conf.pin_bit_mask = 1ULL << CONFIG_IO_PIN_START_STOP_SWITCH;
    // disable pull-down mode
    io_conf.pull_down_en = 0;
    // disable pull-up mode
    io_conf.pull_up_en = 1;
    // configure GPIO with the given settings
    gpio_config(&io_conf);

    // install gpio isr service
    gpio_install_isr_service(0);
    // hook isr handler for specific gpio pin
    gpio_isr_handler_add(CONFIG_IO_PIN_START_STOP_SWITCH, gpio_isr_handler, (void *)CONFIG_IO_PIN_START_STOP_SWITCH);
    // hook isr handler for specific gpio pin

    // Create event queue for rotary encoders
    event_queue = xQueueCreate(EV_QUEUE_LEN, sizeof(rotary_encoder_event_t));

    // Setup rotary encoder library
    ESP_ERROR_CHECK(rotary_encoder_init(event_queue));

    // Add one encoder
    memset(&re, 0, sizeof(rotary_encoder_t));
    re.pin_a = CONFIG_IO_PIN_ROT_A;
    re.pin_b = CONFIG_IO_PIN_ROT_B;
    re.pin_btn = CONFIG_IO_PIN_ROT_SW;
    ESP_ERROR_CHECK(rotary_encoder_add(&re));

    int32_t val = 0;

    ESP_LOGI(TAG, "Initial value: %" PRIi32, val);

    // esp_log_level_set("u8g2_hal", ESP_LOG_ERROR);
    u8g2_esp32_hal_t u8g2_esp32_hal = U8G2_ESP32_HAL_DEFAULT;
#ifdef CONFIG_LCD_TYPE_SPI
    u8g2_esp32_hal.bus.spi.clk = CONFIG_IO_PIN_SPI_CLK;
    u8g2_esp32_hal.bus.spi.mosi = CONFIG_IO_PIN_SPI_MOSI;
    u8g2_esp32_hal.bus.spi.cs = CONFIG_IO_PIN_SPI_CS;
#else
    u8g2_esp32_hal.bus.i2c.sda = CONFIG_IO_PIN_I2C_SDA;
    u8g2_esp32_hal.bus.i2c.scl = CONFIG_IO_PIN_I2C_SCL;
#endif
    u8g2_esp32_hal_init(u8g2_esp32_hal);

    u8g2_Setup_ssd1306_i2c_128x64_noname_f(
        &u8g2,
        U8G2_R0,
        // u8x8_byte_sw_i2c,
        u8g2_esp32_i2c_byte_cb,
        u8g2_esp32_gpio_and_delay_cb); // init u8g2 structure
    u8x8_SetI2CAddress(&u8g2.u8x8, 0x78);

    ESP_LOGI(TAG, "u8g2_InitDisplay");
    u8g2_InitDisplay(&u8g2); // send init sequence to the display, display is in sleep mode after this,

    ESP_LOGI(TAG, "u8g2_SetPowerSave");
    u8g2_SetPowerSave(&u8g2, 0); // wake up display

    displayWidth = u8g2_GetDisplayWidth(&u8g2);
    u8g2_SetContrast(&u8g2, 255);

    ame_ui_DrawVersion();
    vTaskDelay(pdMS_TO_TICKS(2000));

    time_ms = AME_CFG_GetTime();
    if (time_ms > 30000)
    {
        time_ms = 2100;
        AME_CFG_SetTime(2100);
    }
    else if (time_ms < 100)
    {
        time_ms = 100;
        AME_CFG_SetTime(100);
    }

    casesTotal = AME_CFG_GetCasesCount();
    {
        if (casesTotal > 1000)
        {
            casesTotal = 1000;
            AME_CFG_SetCasesCount(1000);
        }
    }

    caseHeight = AME_CFG_GetHeight();

    ps_subscriber_t *subTemp = ps_new_subscriber(1, STRLIST("ame.sensor.temp.1"));
    ps_subscriber_t *subCurr = ps_new_subscriber(1, STRLIST("ame.sensor.current"));
    ps_subscriber_t *subTime = ps_new_subscriber(1, STRLIST("ame.anneal.time"));
    ps_subscriber_t *subDone = ps_new_subscriber(1, STRLIST("ame.anneal.casesDone"));
    ps_subscriber_t *subRemain = ps_new_subscriber(1, STRLIST("ame.anneal.casesRemain"));
    ps_subscriber_t *subMessage = ps_new_subscriber(1, STRLIST("ame.gui.message"));
    ps_msg_t *msg = NULL;

    for (;;)
    {
        ame_ui_UpdateHomeScreen();

        if (xQueueReceive(event_queue, &e, pdMS_TO_TICKS(10)) == pdPASS)
        {
            if (e.type == RE_ET_BTN_CLICKED)
            {
                if (AME_ANNEAL_IsAnnealing() == false)
                {
                    ame_ui_Menu();
                }
                else
                {
                    // Already annealing, so stop annealing
                    AME_ANNEAL_Stop();
                }
            }
            else if (e.type == RE_ET_CHANGED)
            {
            }
        }

        if (xTaskNotifyWait(0, ULONG_MAX, NULL, pdMS_TO_TICKS(10)) == pdTRUE)
        {
            if (esp_log_timestamp() - buttonPressedTimestamp > 500)
            {
                if (gpio_get_level(CONFIG_IO_PIN_START_STOP_SWITCH) == 0)
                {
                    // Start stop button pressed
                    if (AME_ANNEAL_IsAnnealing() == false)
                    {
                        // Not yet annealing, so show startup animation
                        if (ame_ui_ShowStartAnimantion() == true)
                        {
                            // Button held long enough to start annealing
                            AME_ANNEAL_Start();
                            buttonPressedTimestamp = esp_log_timestamp();
                        }
                    }
                    else
                    {
                        // Already annealing, so stop annealing
                        AME_ANNEAL_Stop();
                    }
                }
            }
        }

        msg = ps_get(subTemp, 0);
        if (msg != NULL)
        {
            if (PS_IS_DBL(msg))
            {
                temperature = msg->dbl_val;
            }
        }
        ps_unref_msg(msg);

        msg = ps_get(subCurr, 0);
        if (msg != NULL)
        {
            if (PS_IS_DBL(msg))
            {
                current = msg->dbl_val;
            }
        }
        ps_unref_msg(msg);

        msg = ps_get(subTime, 0);
        if (msg != NULL)
        {
            if (PS_IS_INT(msg))
            {
                time_ms = msg->int_val;
            }
        }
        ps_unref_msg(msg);

        msg = ps_get(subDone, 0);
        if (msg != NULL)
        {
            if (PS_IS_INT(msg))
            {
                casesCount = msg->int_val;
            }
        }
        ps_unref_msg(msg);

        msg = ps_get(subRemain, 0);
        if (msg != NULL)
        {
            if (PS_IS_INT(msg))
            {
                casesRemaining = msg->int_val;
            }
        }
        ps_unref_msg(msg);

        msg = ps_get(subMessage, 0);
        if (msg != NULL)
        {
            if (PS_IS_STR(msg))
            {
                strcpy(infoMessage, msg->str_val);
            }
        }
        ps_unref_msg(msg);
    }
}

